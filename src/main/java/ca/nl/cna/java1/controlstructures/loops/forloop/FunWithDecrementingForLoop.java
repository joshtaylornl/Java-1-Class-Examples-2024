package ca.nl.cna.java1.controlstructures.loops.forloop;

/**
 * A simple program demonstrating how you count down with a for a loop
 *
 * @author josh.taylor
 */
public class FunWithDecrementingForLoop {

    public static void main(String[] args) {
        System.out.println("Fun with counting down a for loop!");

        for(int i = 10; i > 0; i--){
            System.out.printf("%d ", i);
        }

    }

}
